#include <unistd.h>

int			main(int argc, char *argv[])
{
	int i;
	int check;

	i = 0;
	if (argc == 2)
	{
		while (argv[1][i] != '\0')
		{
			if (argv[1][i] == 'a' || argv[1][i] == 'A')
			{
				check = 1;
			}
		i++;
		}
		if (check == 1)
		{
			write (1, "a", 1);
		}
	}
	else
	{
		write (1, "a", 1);
	}
	write (1, "\n", 1);
}