#include <unistd.h>

int		main(int argc, char *argv[])
{
	int i;

	i = 0;
	if (argc == 2)
	{
		while (argv[1][i] != '\0')
		{
			if (argv[1][i] >= 'A' && argv[1][i] <= 'Z')
			{
				write (1, &argv[1][i + 13], 1);
			}
			if (argv[1][i] >= 'a' && argv[1][i] <= 'z')
			{
				write (1, &argv[1][i + 13], 1);
			}
			i++;
		}
	}
	write (1, "\n", 1);
	return (0);
}
